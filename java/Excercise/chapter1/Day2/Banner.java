/*
 * Banner. Write a program Banner.java that takes a string as a command line argument and prints out the string in large letters as below.

    % java Banner "Kevin"
     #    #  ######  #    #     #    #    #
     #   #   #       #    #     #    ##   #
     ####    #####   #    #     #    # #  #
     #  #    #       #    #     #    #  # #
     #   #   #        #  #      #    #   ##
     #    #  ######    ##       #    #    #

* Mimics the Unix utility banner
 */
package Day2;

public class Banner
{
	static char[][] alphabet = {{' ', ' ', '#', '#', ' ', ' ',
								 ' ', '#', ' ', ' ', '#', ' ', 
								 '#', '#', '#', '#', '#', '#',
								 '#', ' ', ' ', ' ', ' ', '#',
								 '#', ' ', ' ', ' ', ' ', '#',
								 '#', ' ', ' ', ' ', ' ', '#'},
								{'#', '#', '#', '#', ' ', ' ',
								 '#', ' ', ' ', ' ', '#', ' ', 
								 '#', '#', '#', '#', ' ', ' ',
								 '#', ' ', ' ', ' ', '#', ' ',
								 '#', ' ', ' ', ' ', ' ', '#',
								 '#', '#', '#', '#', '#', ' '},
								{' ', ' ', '#', '#', '#', '#',
								 ' ', '#', ' ', ' ', ' ', ' ', 
								 '#', ' ', ' ', ' ', ' ', ' ',
								 '#', ' ', ' ', ' ', ' ', ' ',
								 ' ', '#', ' ', ' ', ' ', ' ',
								 ' ', ' ', '#', '#', '#', '#'},
								{'#', '#', '#', '#', ' ', ' ',
								 '#', ' ', ' ', ' ', '#', ' ', 
								 '#', ' ', ' ', ' ', ' ', '#',
								 '#', ' ', ' ', ' ', ' ', '#',
								 '#', ' ', ' ', ' ', '#', ' ',
								 '#', '#', '#', '#', ' ', ' '},
								{'#', '#', '#', '#', '#', '#',
								 '#', ' ', ' ', ' ', ' ', ' ', 
								 '#', '#', '#', '#', '#', ' ',
								 '#', ' ', ' ', ' ', ' ', ' ',
								 '#', ' ', ' ', ' ', ' ', ' ',
								 '#', '#', '#', '#', '#', '#'},
								 {'#', '#', '#', '#', '#', '#',
								  '#', ' ', ' ', ' ', ' ', ' ', 
								  '#', '#', '#', '#', '#', ' ',
								  '#', ' ', ' ', ' ', ' ', ' ',
								  '#', ' ', ' ', ' ', ' ', ' ',
								  '#', ' ', ' ', ' ', ' ', ' '},
								  {' ', ' ', '#', '#', '#', ' ',
								   ' ', '#', ' ', ' ', ' ', ' ', 
								   '#', ' ', ' ', ' ', ' ', ' ',
								   '#', ' ', ' ', '#', '#', '#',
							       ' ', '#', ' ', ' ', '#', '#',
								   ' ', ' ', '#', '#', ' ', '#'},
		};
	public static void main(String[] args)
	{
		String input = args[0].toLowerCase();	// 全部转化为小写字符
		char[] cStr = input.toCharArray();
		
		for (int i = 0; i < 6; i++)
		{
			String output = "";
			for (int j = 0; j < cStr.length; j++)
			{
				int index = cStr[j] - 'a';
				for (int k = 0; k < 6; k++)
				{
					output += alphabet[index][i*6 + k];
				}
				output += '\t';
			}
			System.out.println(output);
		}
	}
}

